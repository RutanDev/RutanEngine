#pragma once

// Typedef						// Size			// Min to Max

// Unsigned int
typedef std::uint8_t	u8;		// 1 byte		// 0 to 255
typedef std::uint16_t	u16;	// 2 bytes		// 0 to 65535
typedef std::uint32_t	u32;	// 4 bytes		// 0 to 4294967295
typedef std::uint64_t	u64;	// 8 bytes		// 0 to 18446744073709551615

// Integer
typedef std::int8_t		i8;		// 1 byte		// -127					to 127
typedef std::int16_t	i16;	// 2 bytes		// -32768				to 32767
typedef std::int32_t	i32;	// 4 bytes		// -2147483648			to 2147483647
typedef std::int64_t	i64;	// 8 bytes		// -9223372036854775808	to 9223372036854775807

// Floatingpoint number
typedef float			f32;	// 4 bytes		//	1.17549e-38	 to 3.40282e+38
typedef double			f64;	// 8 bytes		//	2.22507e-308 to 1.79769e+308


